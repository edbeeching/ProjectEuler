    prev = int(str(prev)[2:])
    for i in range(10, 100):
        to_check = int(str(prev)+str(i))
        
        for cont, _ in pairs:
            if (to_check not in chain and
                cont not in conts and
                to_check in cont.keys() and
                not cont[to_check]):
                #cont[to_check] = True
                
                length = recursive_check(chain+[to_check], conts+[cont])
    return length

